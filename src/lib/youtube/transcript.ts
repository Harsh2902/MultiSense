// Text Cleaning
// =============================================================================

/**
 * Clean transcript text
 * - Decode HTML entities
 * - Remove excessive whitespace
 * - Fix common OCR/auto-caption errors
 */
function cleanTranscriptText(text: string): string {
    return text
        // Decode HTML entities
        .replace(/&amp;/g, '&')
        .replace(/&lt;/g, '<')
        .replace(/&gt;/g, '>')
        .replace(/&quot;/g, '"')
        .replace(/&#39;/g, "'")
        .replace(/&nbsp;/g, ' ')
        // Remove newlines (YouTube captions use \n for line breaks)
        .replace(/\n/g, ' ')
        // Collapse whitespace
        .replace(/\s+/g, ' ')
        // Trim
        .trim();
}

/**
 * Normalize segments by merging very short ones
 * Segments < 2 words are merged with the next segment
 */
function normalizeSegments(segments: TranscriptSegment[]): TranscriptSegment[] {
    const normalized: TranscriptSegment[] = [];
    let pendingText = '';
    let pendingStart = 0;
    let pendingDuration = 0;

    for (const segment of segments) {
        const wordCount = countWords(segment.text);

        if (wordCount < 2 && pendingText === '') {
            // Start accumulating
            pendingText = segment.text;
            pendingStart = segment.startSeconds;
            pendingDuration = segment.durationSeconds;
        } else if (pendingText !== '') {
            // Merge with pending
            pendingText += ' ' + segment.text;
            pendingDuration += segment.durationSeconds;

            // Check if we have enough now
            if (countWords(pendingText) >= 5) {
                normalized.push({
                    text: pendingText.trim(),
                    startSeconds: pendingStart,
                    durationSeconds: pendingDuration,
                });
                pendingText = '';
                pendingDuration = 0;
            }
        } else {
            // Normal segment
            normalized.push(segment);
        }
    }

    // Don't forget remaining pending
    if (pendingText !== '') {
        normalized.push({
            text: pendingText.trim(),
            startSeconds: pendingStart,
            durationSeconds: pendingDuration,
        });
    }

    return normalized;
}

