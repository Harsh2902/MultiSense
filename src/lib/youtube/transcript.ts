function createEmptyResult(error: string): TranscriptResult {
    return {
        segments: [],
        fullText: '',
        wordCount: 0,
        language: 'en',
        available: false,
        error,
    };
}

function countWords(text: string): number {
    return text.trim().split(/\s+/).filter(w => w.length > 0).length;
}

/**
 * Get estimated video duration from transcript
 * (Last segment start + duration)
 */
export function getDurationFromTranscript(transcript: TranscriptResult): number {
    if (transcript.segments.length === 0) {
        return 0;
    }

    const lastSegment = transcript.segments[transcript.segments.length - 1];
    if (!lastSegment) return 0;
    return Math.ceil(lastSegment.startSeconds + lastSegment.durationSeconds);
}

/**
 * Convert transcript to timestamped text format
 */
export function transcriptToTimestampedText(transcript: TranscriptResult): string {
    return transcript.segments
        .map(segment => {
            const minutes = Math.floor(segment.startSeconds / 60);
            const seconds = Math.floor(segment.startSeconds % 60);
            const timestamp = `${minutes}:${seconds.toString().padStart(2, '0')}`;
            return `[${timestamp}] ${segment.text}`;
        })
        .join('\n');
}
